export const dynamic = 'force-dynamic'
export const runtime = 'nodejs'

import { NextRequest, NextResponse } from 'next/server'
import { getServerSession } from 'next-auth'
import { authOptions } from '@/lib/auth'
import { prisma } from '@/lib/prisma'
import { decrypt } from '@/lib/encryption'

export async function POST(request: NextRequest) {
  try {
    // Check authentication
    const session = await getServerSession(authOptions)
    
    if (!session?.user?.email) {
      return NextResponse.json(
        { error: 'Unauthorized - Please sign in' },
        { status: 401 }
      )
    }

    // Get user's GitHub token from database
    const user = await prisma.user.findUnique({
      where: { email: session.user.email },
      select: { githubAccessToken: true, githubUsername: true }
    })

    // Require user to have connected their own GitHub account
    if (!user?.githubAccessToken) {
      return NextResponse.json(
        {
          error: 'GitHub not connected. Please connect your GitHub account to export projects to your own repositories.',
          needsGithubConnection: true,
        },
        { status: 400 }
      )
    }

    // Decrypt the GitHub token for use
    const githubToken = decrypt(user.githubAccessToken)

    // Parse request body
    const body = await request.json()
    const { projectId, projectName, description, files } = body

    // If projectId is provided, fetch project details
    let finalProjectName = projectName
    let finalDescription = description
    let finalFiles = files

    if (projectId) {
      const project = await prisma.project.findUnique({
        where: { id: projectId },
      })

      if (!project) {
        return NextResponse.json(
          { error: 'Project not found' },
          { status: 404 }
        )
      }

      finalProjectName = project.name
      finalDescription = project.description || `${project.name} - Generated by BuildFlow`
      
      // Generate Next.js project structure from project code
      finalFiles = {
        'app/page.tsx': project.code || '',
        'app/layout.tsx': `import './globals.css'

export const metadata = {
  title: '${project.name}',
  description: '${project.description || 'Generated by BuildFlow'}',
}

export default function RootLayout({
  children,
}: {
  children: React.ReactNode
}) {
  return (
    <html lang="en">
      <body>{children}</body>
    </html>
  )
}`,
        'app/globals.css': `@tailwind base;
@tailwind components;
@tailwind utilities;`,
        'package.json': JSON.stringify({
          name: project.name.toLowerCase().replace(/\s+/g, '-'),
          version: '1.0.0',
          description: project.description || `${project.name} - Generated by BuildFlow`,
          scripts: {
            dev: 'next dev',
            build: 'next build',
            start: 'next start',
            lint: 'next lint'
          },
          dependencies: {
            'next': '^14.0.0',
            'react': '^18.0.0',
            'react-dom': '^18.0.0'
          },
          devDependencies: {
            '@types/node': '^20.0.0',
            '@types/react': '^18.0.0',
            '@types/react-dom': '^18.0.0',
            'typescript': '^5.0.0',
            'tailwindcss': '^3.0.0',
            'autoprefixer': '^10.0.0',
            'postcss': '^8.0.0'
          }
        }, null, 2),
        'tailwind.config.js': `/** @type {import('tailwindcss').Config} */
module.exports = {
  content: [
    './pages/**/*.{js,ts,jsx,tsx,mdx}',
    './components/**/*.{js,ts,jsx,tsx,mdx}',
    './app/**/*.{js,ts,jsx,tsx,mdx}',
  ],
  theme: {
    extend: {},
  },
  plugins: [],
}`,
        'README.md': `# ${project.name}

Generated with [BuildFlow](https://buildflow-ai.app) - AI-Powered Code Generation

## ðŸš€ Getting Started

\`\`\`bash
npm install
npm run dev
\`\`\`

Open [http://localhost:3000](http://localhost:3000) in your browser.

---

Made with â¤ï¸ by BuildFlow`,
        '.gitignore': `# dependencies
/node_modules
/.pnp
.pnp.js

# testing
/coverage

# next.js
/.next/
/out/

# production
/build

# misc
.DS_Store
*.pem

# debug
npm-debug.log*
yarn-debug.log*
yarn-error.log*

# local env files
.env*.local

# vercel
.vercel

# typescript
*.tsbuildinfo
next-env.d.ts`
      }
    }

    if (!finalProjectName) {
      return NextResponse.json(
        { error: 'Project name is required' },
        { status: 400 }
      )
    }

    // Create repository name (lowercase, no spaces)
    const repoName = finalProjectName.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '')

    // Validate repository name to prevent unsafe values in the GitHub API URL
    const repoNamePattern = /^[a-z0-9-]+$/
    if (!repoName || repoName.length > 100 || !repoNamePattern.test(repoName)) {
      return NextResponse.json(
        { error: 'Invalid repository name derived from project name' },
        { status: 400 }
      )
    }

    // Step 1: Create the repository
    console.log('Creating GitHub repository:', repoName)
    
    const createRepoResponse = await fetch('https://api.github.com/user/repos', {
      method: 'POST',
      headers: {
        'Authorization': `token ${githubToken}`,
        'Accept': 'application/vnd.github.v3+json',
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        name: repoName,
        description: finalDescription || `${finalProjectName} - Generated by BuildFlow`,
        private: false,
        auto_init: true, // Initialize with README
      }),
    })

    if (!createRepoResponse.ok) {
      const error = await createRepoResponse.json()
      console.error('GitHub API error:', error)
      
      // Handle specific errors
      if (error.errors && error.errors[0]?.message?.includes('already exists')) {
        return NextResponse.json(
          { error: `Repository '${repoName}' already exists in your GitHub account. Please choose a different name.` },
          { status: 409 }
        )
      }

      // Handle token expired/invalid
      if (error.message?.includes('Bad credentials') || createRepoResponse.status === 401) {
        // Clear the invalid token
        if (user?.githubAccessToken) {
          await prisma.user.update({
            where: { email: session.user.email },
            data: { githubAccessToken: null, githubUsername: null }
          })
        }
        return NextResponse.json(
          { 
            error: 'GitHub authorization expired. Please reconnect your GitHub account.',
            needsGithubConnection: true
          },
          { status: 401 }
        )
      }
      
      return NextResponse.json(
        { error: error.message || 'Failed to create repository' },
        { status: createRepoResponse.status }
      )
    }

    const repo = await createRepoResponse.json()
    console.log('Repository created:', repo.html_url)

    // Step 2: Upload files to the repository
    if (finalFiles && Object.keys(finalFiles).length > 0) {
      console.log('Uploading files to repository...')
      
      // Get the default branch (usually 'main' or 'master')
      const defaultBranch = repo.default_branch || 'main'
      
      // Upload each file
      for (const [path, content] of Object.entries(finalFiles)) {
        try {
          const uploadResponse = await fetch(
            `https://api.github.com/repos/${repo.owner.login}/${repoName}/contents/${path}`,
            {
              method: 'PUT',
              headers: {
                'Authorization': `token ${githubToken}`,
                'Accept': 'application/vnd.github.v3+json',
                'Content-Type': 'application/json',
              },
              body: JSON.stringify({
                message: `Add ${path}`,
                content: Buffer.from(content as string).toString('base64'),
                branch: defaultBranch,
              }),
            }
          )

          if (!uploadResponse.ok) {
            const uploadError = await uploadResponse.json()
            console.error(`Failed to upload ${path}:`, uploadError)
          } else {
            console.log(`Uploaded: ${path}`)
          }
        } catch (uploadError) {
          console.error(`Error uploading ${path}:`, uploadError)
        }
      }
    }

    // Return success with repository URL
    return NextResponse.json({
      success: true,
      repoUrl: repo.html_url,
      repoName: repoName,
      owner: repo.owner.login,
      message: `Repository created successfully in ${repo.owner.login}'s GitHub!`,
    })

  } catch (error: any) {
    console.error('GitHub integration error:', error)
    return NextResponse.json(
      { error: error.message || 'Internal server error' },
      { status: 500 }
    )
  }
}
